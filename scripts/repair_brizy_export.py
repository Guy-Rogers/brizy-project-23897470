#!/usr/bin/env python3
from __future__ import annotations

import html
import re
from pathlib import Path
from xml.sax.saxutils import escape


SITE_URL = "https://insightcoaching-ic.netlify.app"
DEFAULT_LANG = "de"
LANGS = ("de", "en")

ROOT = Path(__file__).resolve().parents[1]


def page_files() -> list[Path]:
    files = []
    for path in sorted(ROOT.glob("*.html")):
        if re.match(r"^\d+-", path.name):
            continue
        files.append(path)
    return files


def slug_for(path: Path) -> str:
    return "" if path.name == "index.html" else path.stem


def canonical_path(slug: str) -> str:
    if slug:
        return f"/{DEFAULT_LANG}/{slug}"
    return f"/{DEFAULT_LANG}/"


def canonical_url(slug: str) -> str:
    return SITE_URL + canonical_path(slug)


def replace_attr_tag(content: str, pattern: str, replacement: str) -> str:
    if re.search(pattern, content, flags=re.IGNORECASE):
        return re.sub(pattern, replacement, content, count=1, flags=re.IGNORECASE)
    return content


def normalize_href(href: str, slugs: set[str]) -> str:
    if not href.startswith("/") or href.startswith("//"):
        return href

    path, suffix = href, ""
    for sep in ("#", "?"):
        if sep in path:
            path, rest = path.split(sep, 1)
            suffix = sep + rest
            break

    clean = path.strip("/")
    if not clean:
        return href

    parts = clean.split("/")
    last = parts[-1]
    if last == "insight-coaching-thomas-jakel":
        return f"/{DEFAULT_LANG}/{suffix}"
    if last in slugs:
        return f"/{DEFAULT_LANG}/{last}{suffix}"
    if len(parts) == 2 and parts[0] in LANGS and parts[1] in slugs:
        return f"/{parts[0]}/{parts[1]}{suffix}"
    return href


def repair_html_files(files: list[Path], slugs: set[str]) -> None:
    for path in files:
        slug = slug_for(path)
        content = path.read_text(encoding="utf-8", errors="ignore")

        content = re.sub(
            r'<link\s+rel=["\']canonical["\'][^>]*>',
            f'<link rel="canonical" href="{canonical_url(slug)}">',
            content,
            count=1,
            flags=re.IGNORECASE,
        )
        content = re.sub(
            r'<meta\s+property=["\']og:url["\']\s+content=["\'][^"\']*["\']\s*/?>',
            f'<meta property="og:url" content="{canonical_url(slug)}">',
            content,
            count=1,
            flags=re.IGNORECASE,
        )

        def href_repl(match: re.Match[str]) -> str:
            quote = match.group(1)
            href = html.unescape(match.group(2))
            return f'href={quote}{normalize_href(href, slugs)}{quote}'

        content = re.sub(r'href=(["\'])([^"\']+)\1', href_repl, content)
        path.write_text(content, encoding="utf-8")


def collect_nested_routes(files: list[Path], slugs: set[str]) -> set[tuple[str, str]]:
    routes: set[tuple[str, str]] = set()
    for path in files:
        content = path.read_text(encoding="utf-8", errors="ignore")
        for href in re.findall(r'href=["\']([^"\']+)["\']', content):
            if not href.startswith("/") or href.startswith("//"):
                continue
            clean = href.split("#", 1)[0].split("?", 1)[0].strip("/")
            parts = clean.split("/")
            if len(parts) >= 2 and parts[-1] in slugs:
                routes.add(("/" + clean, f"/{DEFAULT_LANG}/{parts[-1]}"))
    return routes


def write_redirects(files: list[Path]) -> None:
    slugs = {slug_for(path) for path in files if slug_for(path)}
    lines: list[str] = [
        "# Generated by scripts/repair_brizy_export.py",
        f"/ /{DEFAULT_LANG}/ 301",
        f"/{DEFAULT_LANG} /{DEFAULT_LANG}/ 301",
        f"/en /en/ 301",
        f"/{DEFAULT_LANG}/ /index.html 200",
        "/en/ /index.html 200",
    ]

    for slug in sorted(slugs):
        lines.append(f"/{slug} /{DEFAULT_LANG}/{slug} 301")
        for lang in LANGS:
            lines.append(f"/{lang}/{slug} /{slug}.html 200")

    for source, target in sorted(collect_nested_routes(files, slugs)):
        if source != target:
            lines.append(f"{source} {target} 301")

    (ROOT / "_redirects").write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_sitemap(files: list[Path]) -> None:
    entries: list[str] = []
    for path in files:
        slug = slug_for(path)
        for lang in LANGS:
            if slug:
                loc = f"{SITE_URL}/{lang}/{slug}"
            else:
                loc = f"{SITE_URL}/{lang}/"
            alternates = []
            for alt_lang in LANGS:
                alt = f"{SITE_URL}/{alt_lang}/{slug}" if slug else f"{SITE_URL}/{alt_lang}/"
                alternates.append(
                    f'<xhtml:link rel="alternate" hreflang="{alt_lang}" href="{escape(alt)}" />'
                )
            entries.append(
                "<url>"
                f"<loc>{escape(loc)}</loc>"
                + "".join(alternates)
                + "<changefreq>weekly</changefreq><priority>0.8</priority>"
                "</url>"
            )

    sitemap = (
        '<?xml version="1.0" encoding="UTF-8"?>'
        '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9" '
        'xmlns:xhtml="http://www.w3.org/1999/xhtml">'
        + "".join(entries)
        + "</urlset>\n"
    )
    (ROOT / "sitemap.xml").write_text(sitemap, encoding="utf-8")


def main() -> None:
    files = page_files()
    slugs = {slug_for(path) for path in files if slug_for(path)}
    repair_html_files(files, slugs)
    write_redirects(files)
    write_sitemap(files)
    print(f"repaired_html_files={len(files)}")
    print(f"slugs={len(slugs)}")


if __name__ == "__main__":
    main()
